import { mkdir, writeFile } from "node:fs/promises";

const SOURCE =
  "https://en.wikipedia.org/w/index.php?title=Template:2026_FIFA_World_Cup_third-place_table&action=raw";

const groupColumns = ["A", "B", "C", "D", "E", "F", "G", "H", "I", "J", "K", "L"];
const assignmentColumns = ["1A", "1B", "1D", "1E", "1G", "1I", "1K", "1L"];

function cleanCell(cell) {
  return cell
    .replace(/<[^>]*>/g, "")
    .replace(/\{\{[^}]+\}\}/g, "")
    .replace(/'''/g, "")
    .replace(/\[\[|\]\]/g, "")
    .replace(/\|/g, "")
    .trim();
}

function parseRows(raw) {
  const rows = [];
  const lines = raw.split("\n");

  for (let index = 0; index < lines.length; index += 1) {
    if (!/!\s*scope="row"/.test(lines[index])) {
      continue;
    }

    const cells = [];
    let cursor = index + 1;

    while (cursor < lines.length && !/^\s*\|-/.test(lines[cursor]) && !/^\s*\|}/.test(lines[cursor])) {
      const line = lines[cursor].trim();
      if (line.startsWith("|")) {
        const parts = line
          .replace(/^\|+/, "")
          .split("||")
          .map(cleanCell);
        cells.push(...parts);
      }
      cursor += 1;
    }

    if (cells.length >= 20) {
      const qualified = groupColumns.filter((group, cellIndex) => cells[cellIndex] === group);
      const assignments = Object.fromEntries(
        assignmentColumns.map((slot, cellIndex) => [slot, cleanCell(cells[12 + cellIndex]).replace(/^3/, "")])
      );
      const key = qualified.join("");

      if (key.length === 8 && Object.values(assignments).every((value) => /^[A-L]$/.test(value))) {
        rows.push([key, assignments]);
      }
    }
  }

  return Object.fromEntries(rows);
}

const response = await fetch(SOURCE);
if (!response.ok) {
  throw new Error(`Unable to fetch third-place table: ${response.status} ${response.statusText}`);
}

const map = parseRows(await response.text());
const entries = Object.keys(map).length;

if (entries !== 495) {
  throw new Error(`Expected 495 combinations, received ${entries}`);
}

const banner = `// Generated by scripts/build-third-place-map.mjs from ${SOURCE}\n`;
const content = `${banner}export type ThirdPlaceWinnerSlot = "1A" | "1B" | "1D" | "1E" | "1G" | "1I" | "1K" | "1L";\n\nexport const thirdPlaceMap: Record<string, Record<ThirdPlaceWinnerSlot, string>> = ${JSON.stringify(
  map,
  null,
  2
)};\n`;

await mkdir(new URL("../src/data/", import.meta.url), { recursive: true });
await writeFile(new URL("../src/data/thirdPlaceMap.ts", import.meta.url), content);
console.log(`Generated ${entries} third-place combinations.`);
